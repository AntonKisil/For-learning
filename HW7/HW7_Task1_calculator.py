def add(a,b):
    """
    Function add two numbers
    """
    return a+b

def sub(a,b):
    """
    The function subtracts the second number from the first
    """
    return a-b

def mult(a,b):
    """
    The function multiplies numbers
    """
    return a*b

def div(a,b):
    """
    The function divides the first number by the second
    """
    return a/b
